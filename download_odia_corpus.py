def create_extended_corpus():
    """
    Create an extended corpus by splitting texts into words and small phrases.
    """
    base_texts = download_odia_wikipedia()
    
    # Split all texts into words and short phrases
    extended_texts = []
    seen_texts = set()  # Track to avoid exact duplicates
    
    for text in base_texts:
        # Split by Odia sentence separator first
        sentences = text.split('।')
        for sentence in sentences:
            sentence = sentence.strip()
            if not sentence:
                continue
            
            # Split sentence into words
            words = sentence.split()
            
            # Add individual words (if long enough)
            for word in words:
                if len(word) >= 3 and word not in seen_texts:
                    extended_texts.append(word)
                    seen_texts.add(word)
            
            # Add 2-word phrases
            for i in range(len(words) - 1):
                phrase = ' '.join(words[i:i+2])
                if phrase not in seen_texts:
                    extended_texts.append(phrase)
                    seen_texts.add(phrase)
            
            # Add 3-word phrases
            for i in range(len(words) - 2):
                phrase = ' '.join(words[i:i+3])
                if phrase not in seen_texts:
                    extended_texts.append(phrase)
                    seen_texts.add(phrase)
            
            # Add full sentence (if not too long and not seen)
            full_sentence = sentence + ' ।'
            if len(words) <= 8 and full_sentence not in seen_texts:
                extended_texts.append(full_sentence)
                seen_texts.add(full_sentence)
    
    return extended_texts


def save_corpus(texts: list, filepath: str = "odia_corpus.txt"):
    """Save the corpus to a file."""
    with open(filepath, 'w', encoding='utf-8') as f:
        for text in texts:
            f.write(text.strip() + '\n\n')
    print(f"Corpus saved to {filepath}")
    print(f"Total texts: {len(texts)}")
    print(f"Total characters: {sum(len(t) for t in texts)}")


if __name__ == "__main__":
    print("Creating Odia language corpus...")
    texts = create_extended_corpus()
    save_corpus(texts, "odia_corpus.txt")
    print("Corpus creation complete!")

